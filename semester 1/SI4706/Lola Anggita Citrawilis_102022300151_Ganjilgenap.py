# codingan yang harus menginput data integer terlebih dahulu
bilangan = int(input("Masukkan bilangan: "))

if bilangan % 2 == 0:
    print(f"{bilangan} adalah bilangan genap.")
else:
    print(f"{bilangan} adalah bilangan ganjil.")


